function inPolygon(x, y, xp, yp) {
   const npol = xp.length;
   let j = npol - 1;
   let c = false;

   for (var i = 0; i < npol; i++) {
      if (
         (((yp[i] <= y) && (y < yp[j])) || ((yp[j] <= y) && (y < yp[i]))) 
         && (x > (xp[j] - xp[i]) * (y - yp[i]) / (yp[j] - yp[i]) + xp[i])
      ) {
         c = !c;
      }

      j = i;
   }

   return c;
}
